int ScnMapEffect(int no)
{
	static int toheart_effect[] = {
		LVNS_EFFECT_FADE_PALETTE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_FADE_MASK,
		LVNS_EFFECT_FADE_SQUARE,
		LVNS_EFFECT_WIPE_SQUARE_LTOR,
		LVNS_EFFECT_FADE_MASK,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_WIPE_MASK_LTOR,
		LVNS_EFFECT_SLIDE_LTOR,

		LVNS_EFFECT_WIPE_LTOR,
		LVNS_EFFECT_NO_EFFECT,
		LVNS_EFFECT_FADE_MASK,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_TOP_SCROLL,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,

		LVNS_EFFECT_BLOOD,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,
		LVNS_EFFECT_NONE,

		LVNS_EFFECT_NONE,
		LVNS_EFFECT_FADE_PALETTE
	};

	if(no == 255)
	{
		return LVNS_EFFECT_NONE;
	}

	assert(no < sizeof(toheart_effect) / sizeof(int));
	int tmp = toheart_effect[no];
	int ret = LVNS_EFFECT_NONE;

	switch(tmp)
	{
	case LVNS_EFFECT_NONE:             ret =  0; break;
	case LVNS_EFFECT_FADE_PALETTE:     ret =  1; break;
	case LVNS_EFFECT_FADE_MASK:        ret =  2; break;
	case LVNS_EFFECT_FADE_SQUARE:      ret =  3; break;
	case LVNS_EFFECT_WIPE_SQUARE_LTOR: ret =  4; break;
	case LVNS_EFFECT_WIPE_MASK_LTOR:   ret =  5; break;
	case LVNS_EFFECT_SLIDE_LTOR:       ret =  6; break;
	case LVNS_EFFECT_WIPE_LTOR:        ret =  7; break;
	case LVNS_EFFECT_NO_EFFECT:        ret =  8; break;
	case LVNS_EFFECT_TOP_SCROLL:       ret =  9; break;
	case LVNS_EFFECT_BLOOD:            ret = 10; break;
	}

	return ret;
}
