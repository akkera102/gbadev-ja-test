//---------------------------------------------------------------------------
void ScnOpen(const char* fname)
{
	// get filesize
	FILE* fp = fopen(fname, "rb");

	if(fp == NULL)
	{
		fprintf(stderr, "Open Error fp %s\n", fname);
		exit(1);
	}

	fseek(fp, 0, SEEK_END);
	Scn.size = ftell(fp);

	// read file
	Scn.p = calloc(Scn.size, sizeof(u_char));

	if(Scn.p == NULL)
	{
		fprintf(stderr, "Open Calloc Error\n");
		exit(1);
	}

	fseek(fp, 0, SEEK_SET);
	fread(Scn.p, 1, Scn.size, fp);

	fclose(fp);
}
//---------------------------------------------------------------------------
void ScnExt()
{
	u_char* pE = Scn.p + ((Scn.p[1] << 8) | Scn.p[0]) * 0x10;
	u_char* pM = Scn.p + ((Scn.p[3] << 8) | Scn.p[2]) * 0x10;

	long sizeE = (pE[3] << 24) | (pE[2] << 16) | (pE[1] << 8) | pE[0];
	long sizeM = (pM[3] << 24) | (pM[2] << 16) | (pM[1] << 8) | pM[0];

	// イベントとメッセージ解凍
	Scn.pE = ScnLzs(pE + 4, sizeE);
	Scn.pM = ScnLzs(pM + 4, sizeM);

	Scn.sizeEvent   = sizeE;
	Scn.sizeMessage = sizeM;
}
//---------------------------------------------------------------------------
u_char* ScnLzs(u_char* p, long DataSize)
{
	u_char* q = calloc(DataSize, sizeof(u_char));

	if(q == NULL)
	{
		fprintf(stderr, "ScnLzs Calloc Error\n");
		exit(1);
	}

	u_char* pLoadBuff = p;
	u_char* pSaveBuff = q;

	u_long  Index, LIndex, Len, FlagCount;
	u_short LFlag;
	u_char  TextBuff[0x1011], Flag;

	// テキストバッファのクリア 
	memset(TextBuff, 0, sizeof(TextBuff));

	// テキストバッファの書き込み位置の設定
    Index = 0xfee;
	FlagCount = 0;

	while (DataSize > 0)
	{
		if (FlagCount-- > 0)
		{
			Flag <<= 1;
		}
		else
		{
			// フラグの取得 
			Flag = ~(*(pLoadBuff++));
			FlagCount = 7;
		}

		if(Flag & 0x80)
		{
			TextBuff[Index++] = *pSaveBuff++ = ~(*pLoadBuff++);
			Index &= 0x0fff;
			DataSize--;
		}
		else
		{
			// 以前に出現した場所と長さの情報の取得(2 byte)
			LFlag = ~(*(pLoadBuff) + (*(pLoadBuff + 1) << 8));
			pLoadBuff += 2;

			// 長さ 
			Len = (LFlag & 0xf) + 3;
			DataSize -= Len;

			// 以前出現した位置
			LIndex = LFlag >> 4;

			// 以前出現した位置からコピー
			while(Len-- > 0)
			{
				TextBuff[Index++] = *(pSaveBuff++) = TextBuff[LIndex++];
				LIndex &= 0x0fff;
				Index  &= 0x0fff;
			}
		}
	}

	return q;
}
//---------------------------------------------------------------------------
void ScnSave(const char* fname)
{
	char sname[13];
	strncpy(sname, fname, 13);

	char* p = strchr(sname, '.') + 1;
	p[0] = 't';
	p[1] = 'x';
	p[2] = 't';

	FILE* fp = fopen(sname, "wb");
	if(fp == NULL)
	{
		printf("Save Error %s\n", sname);
		exit(1);
	}

	ScnSaveSub('E', Scn.pE, Scn.sizeEvent, fp);
	ScnSaveSub('M', Scn.pM, Scn.sizeMessage, fp);

	fclose(fp);
}
